export function getExtensionSearchText(extension: AgentExtension): string {
  return [
    extension.name,
    extension.kind,
    extension.platform,
    extension.category,
    extension.maturity,
    extension.summary,
    ...extension.whatItDoes,
    extension.install,
    extension.usage,
    extension.koreanNote ?? '',
    extension.rank?.toString() ?? '',
    extension.rankReason ?? '',
    extension.installDifficulty ?? '',
    extension.adoptionSignal ?? '',
    extension.lastVerified ?? '',
    ...(extension.installSteps ?? []),
    ...(extension.bestFor ?? []),
    ...extension.tags,
  ].join(' ')
}

export function getExtensionStats() {
  return {
    total: agentExtensions.length,
    kinds: new Set(agentExtensions.map((e) => e.kind)).size,
    platforms: new Set(agentExtensions.map((e) => e.platform)).size,
    categories: new Set(agentExtensions.map((e) => e.category)).size,
    official: agentExtensions.filter((e) => e.maturity === '공식').length,
    ranked: agentExtensions.filter((e) => typeof e.rank === 'number').length,
  }
}

export function getExtensionRankGrade(extension: Pick<AgentExtension, 'rank'>): ExtensionRankGrade {
  if (!extension.rank) return '미분류'
  if (extension.rank <= 8) return 'S'
  if (extension.rank <= 18) return 'A'
  if (extension.rank <= 28) return 'B'
  return 'C'
}
